/*
adding two integers
 */
public class AddTwoInteger {
    public static void main(String[] args) {
        int a = 5;
        int b = 6;
        System.out.println(getSum(a, b));
    }

    public static int getSum(int a, int b) {
        return (a + b);
    }
}
